class RequestTest extends \PHPUnit_Framework_TestCase
{
    public function testClone() {
        $request = new Request();
        $clone = clone $request;

        $this->assertThat(
            $request->getHandle(),
            $this->logicalNot(
                $this->equalTo($clone->getHandle())
            )
        );
    }
}

